const CALENDAR_THEMES_KEY =
    "calendarThemes";

export function getCalendarTheme(
    calendarName
) {

  try {

    const calendarThemes =
        JSON.parse(
            localStorage.getItem(
                CALENDAR_THEMES_KEY
            )
        ) || {};

    const themeName =
        calendarThemes[calendarName];

    return (
        themes.find(
            (t) =>
                t.name === themeName
        ) || themes[0]
    );

  } catch {

    return themes[0];
  }
}

export function setCalendarTheme(
    calendarName,
    themeName
) {

  try {

    const calendarThemes =
        JSON.parse(
            localStorage.getItem(
                CALENDAR_THEMES_KEY
            )
        ) || {};

    calendarThemes[calendarName] =
        themeName;

    localStorage.setItem(
        CALENDAR_THEMES_KEY,
        JSON.stringify(calendarThemes)
    );

  } catch (e) {

    console.error(
        "Failed to save theme",
        e
    );
  }
}